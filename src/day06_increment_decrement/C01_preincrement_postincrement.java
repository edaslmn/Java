package day06_increment_decrement;

public class C01_preincrement_postincrement {

    public static void main(String[] args) {

        //


       int a = 10;

       int b = 20;

       a++;

        System.out.println("13. satırda a:" + a);

        ++a;

        System.out.println(" 17. satırda a:" +a);

        b--;

        System.out.println("22. satırda b:" +b);

        --b;

        System.out.println("25. satırda b:" +b);

        /* eğer bulunduğu satırda yalnız ise
        a++ ile ++a
        b--ile
         */





    }
}
